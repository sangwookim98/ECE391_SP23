/* 
 * fruit_text_to_graphic
 *   DESCRIPTION: similar to text_graphic_img_gen function where the function takes in a string of letters to print out as the transparent text
 *                and converts into binary version of the letter by indexing font_data.
 *                It then checks for each place of the converted text with a 0x80 mask using an AND bitwise operation.
 *                If it detects 1, it will color red, 0 will color green. 
 *   INPUTS: *letter -- pointer to an array of text to be printed out in the status bar
 *           *buf -- pointer to the buffer that is used to store converted and colored centralized text, whatever is stored in the buffer is the one getting printed out in the status bar
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: buffer is only created temporary only when the function calls to create and put text into status bar.  
 */

/* function for CP2 */
/* function for text to graphics image generation*/
void fruit_text_to_graphic(char* letter, unsigned char* buf, unsigned char* buf_2) {
    /* local variables declaration */
    int x, y, z;
    int max_char = strlen(letter);                              /* given # of string in the status bar */
    int text_height = FONT_HEIGHT;
    int each_char = FONT_WIDTH;

    memcpy(buf, buf_2, FONT_WIDTH * str_width_limit * FONT_HEIGHT);                  /* copying entire pixel dimension to another array */

    for (x = 0; x < max_char; x++) {                            /* grabs which letter in the string/array of char */
        for (y = 0; y < text_height; y++) {
                                                                /* x*2 for to find out which pair of 0's */
            unsigned char mask_hex = MSB_mask;                      /* 0x80 for bit masking from left to right */
            for (z = 0; z < each_char; z++) {
                                                                /* z/4 to figure out which pair of 0s in the pair */
                int buffer_idx;
                /*  buffer index calculation explanation:
                 *  -> x * 2  => each character has 2 pairs for 4 numbered planes
                 *  -> y * 80 => 40 characters in a row with pairs 
                 *  -> z / 4  => choosing which pair of plane within a character
                 *  -> z % 4 * 1440 => choosing which plane in buffer */
                int centering_z;
                centering_z = (str_array - (max_char * FONT_WIDTH))/2;                             /* 14 * 8 represents the length of the message screen, this is the centering z offset calculation */
                centering_z += z;

                buffer_idx = x * FONT_WIDTH + (y * FONT_WIDTH * str_width_limit + centering_z);                 /* 8: width of the pixel text */
               
                int i;
                i = (int) letter[x];                                        /* i gives font data for letter */
                unsigned char gen_letter = font_data[i][y];                 /* letter converted using indexing font data*/

                /* checking for 0's and 1's within the selected letter */
                unsigned char and_result;
                and_result = mask_hex & gen_letter;                         /* masking by AND bitwise operaiton */
                if (and_result != 0) {
                    buf[buffer_idx] += transparent_offset;                                /* set to background color*/
               }

                mask_hex = mask_hex >> 1;                                   /* checking next bit if 1 or 0 */
                }

                
            }
        }
}
